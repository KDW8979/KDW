# List 데이터 자료형 살펴보기
# - List와 str
#----------------------------------------------------------
msg="Happy"

# str => list 형변환
datas=list(msg)
print(datas)

msg=['Happy']
datas=list(msg)
print(datas)
# list 함수는 반복이 가능한 (iterable) 문자열과 리스트만 가능